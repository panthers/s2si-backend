package com.cp.s2si.exception;

/**
 * 
 * @author panther
 *
 */
public class MessageConversionToTaskException extends Exception {

	private static final long serialVersionUID = 757787573111675094L;

	public MessageConversionToTaskException() {
		super();
	}

	public MessageConversionToTaskException(String message, Throwable cause) {
		super(message, cause);
	}

	public MessageConversionToTaskException(String message) {
		super(message);
	}

	public MessageConversionToTaskException(Throwable cause) {
		super(cause);
	}
	
}
